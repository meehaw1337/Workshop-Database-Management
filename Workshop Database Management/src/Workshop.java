public class Workshop {
    String WorkshopID;
    String DayID;
    String NumberOfSeats;
    String WorkshopName;

    public Workshop(String workshopID, String dayID, String numberOfSeats, String workshopName) {
        WorkshopID = workshopID;
        DayID = dayID;
        NumberOfSeats = numberOfSeats;
        WorkshopName = workshopName;
    }
}
